package Logic;

public class Target {
	
	int actualValue, i, j;
	
	public Target(int i, int j, int value ) {
		this.i = i;
		this.j = j;
		actualValue = value;
	}

	public int getActualValue() {
		return actualValue;
	}

	public void setActualValue(int actualValue) {
		this.actualValue = actualValue;
	}

	public int getI() {
		return i;
	}

	public void setI(int i) {
		this.i = i;
	}

	public int getJ() {
		return j;
	}

	public void setJ(int j) {
		this.j = j;
	}
	
	
	
	

}
